        <h2>Definition of a Mastermind</h2>
        <P >A mastermind...sounds like an evil T.V.
            Character or even that guy who's amazing at everything he does. Yeah,
            I'm not talking about those kinds of masterminds. What I'm talking
            about is a group of people who get together to strengthen each others
            business. A mastermind is the power of multiple people contributing
            to a better idea. The strength being not in one but in many. It's
            difficult to put the power of masterminds into action with just a
            vague understanding of what they are. By learning a few key elements
            about masterminds, we'll uncover what we'll need to make our
            masterminds successful.</P>
        <P ><BR>
        </P>
    <img src="<?php echo Config::get('URL'); ?>images/MisterMastermind.jpg" alt="Mister Mastermind" >
            <h3>Who is in a Mastermind?</h3>
                <P >Just about anybody can be in a
                    mastermind. It doesn't matter if you are just starting out, or if
                    you're a multimillionaire. However, I wouldn't put both of those
                    people in the same group. You may be wondering why, but that's a
                    topic for another time.</P>
            <h3>How Can a Mastermind Help You?</h3>
                <P >If you aren't sure if you need a
                    mastermind, here are a few things that you may want to consider.</P>

                    <h4>Masterminds Will Give You
                            Assignments</h4>
                        <P >Yes, it's just like you were back in
                            school. No really, your group may have some great suggestions for
                            you to implement. For example, you may be struggling with how are
                            you going to make money with your site. Your group may suggest that
                            you create a course, or write a book to sell on your site.</P>
                        <P ><BR>Heck, there are so many good
                            suggestions that can come from your group: start journaling every
                            day, try taking cold showers for the next 30 days, do the dishes
                            before you work on your business. Endless possibilities...endless.</P>
                    <h4>A Mastermind Can Validate Your
                            Business Plan</h4>
                        <P >Some people are really fast action
                            takers. It may seem that taking fast action is a great business
                            attribute, and it is...except you may be taking the wrong actions
                            without even realizing it. I love bouncing ideas off of my
                            mastermind before I take action, because it gives me a multiview
                            perspective on what I am about to do, and often they can see things
                            that I can not.</P>
                    <h4>Masterminds Provide
                            Accountability</h4>
                        <P >This is something for everyone. It
                            doesn't matter how much of an action taker you are, you still need
                            accountability. I think of it as a hack on my self pride. It's a
                            chance to say that I'm serious about success, and that I follow
                            through. Respect and acknowledgment are trivial matters, but non
                            the less...they still matter.</P>
                    <h4>Masterminds Are a Great Way to
                            Connect</h4>
                        <P >Meeting other entrepreneurs will help
                            stir you up to greatness. Seeing that your friends are taking
                            action, creating value, and getting paid for it is so encouraging
                            to see. It can be infectious, spurring you on even more.</P>

            <h3>Types of People Who Should
                    Mastermind
                </h3>
                <P >What kinds of People Join Masterminds?
                    All Kinds.</P>

                    <h4>How Many People Should Be in a
                            Group</h4>
                        <P >The number of people that are in the
                            group often depends on the goal of the group. Large groups are good
                            sometimes because they usually are led by experts. Often times
                            these groups are led by people who have a large audience. The same
                            person who may run a large mastermind may also find herself in a
                            small mastermind or two. One where she can receive input for her
                            own business.</P>

                            <h5>Large Masterminds</h5>
                                <P >A mastermind can have many people,
                                    like 15 to 20 or more, or as little as 2 members. Each has it's
                                    own advantages. For example, when your in a large group you many
                                    not get much time to focus on your business, but you'll gain much
                                    in listening to strategies that others are giving.
                                </P>
                            <h5>Small Masterminds</h5>
                                <P >These groups work in an almost
                                    completely opposite way as large mastermind groups. You can get
                                    more time focusing on your business, but the smaller the group,
                                    the less input you will get. That isn't always a bad thing
                                    sometimes. Too much input can be so confusing that you wind up
                                    doing nothing.</P>

                    <h4>Business Types Can Be Mixed</h4>
                        <P >The types of businesses vary greatly.
                            There may be professional clowns in a mastermind with a C.E.O. of
                            the latest tech startup. Yes, it's a good thing. An example of why
                            could be that the C.E.O. needs some advice on how to improve her
                            performance on stage, and the clown is an expert in stage
                            performance.</P>

            <h3>Types of Masterminds</h3>
                <P >It takes all types.<BR>No seriously,
                    masterminds vary also by how they meet. Some meet online while
                    others meet in person.</P>

                    <h4>Online Groups</h4>
                        <P >Most masterminds meet online. This is
                            really great because you can meet people from all over the world. I
                            find this to be helpful for me because I work nights and it means
                            that I can have a mastermind with someone on my nights off at 3:00
                            am in the morning (I don't, but I could).</P>

                            <h5>Regular, Live Meetings</h5>
                                <P >Your group may decide to meet once a
                                    month, every week, or even the first and third week of the
                                    month...heck they can meet an any interval. It just depends on the
                                    group's needs.<BR><BR>I believe that every group needs a place to
                                    organize, not just through email. It's so difficult to use email
                                    to coordinate anything. That's why I suggest creating a Facebook
                                    group. Just about everyone is on Facebook, so it probably is the
                                    easiest solution for your group.
                                </P>
                            <h5>Virtual Meetings</h5>
                                <P >Meeting through an online group page
                                    or app only is what I like to call a virtual group. This type of
                                    group doesn't have much face to face or direct dialog. Most of the
                                    interaction takes place through email, or a group page, or an
                                    online app such as Slack (insert affiliate link).</P>
                            <h5>Tools</h5>
                                <P >So I've already mentioned two of my
                                    favorite tools for organizing a group together (Slack and
                                    Facebook), and of course my not so favorite tool...email. The
                                    truth is that creativity is an awesome thing, and you may find
                                    that you'll need to put in a little bit of creativity to keep the
                                    group organized. What I'm trying to say is that there are no
                                    perfect solutions, so keep an eye out , experiment, do what ever
                                    you might think works best.<BR><BR>If you are planning on having a
                                    scheduled meeting time, then you may want to experiment with
                                    different call/conferencing programs. Some options include,
                                    conference calls via phone, Skype, and Google Hangouts. I suggest
                                    Skype, because it just works.</P>

                    <h4>In Person Mastermind Meetings</h4>
                        <P >The last type of group that I would
                            like to mention is a group that meets in person. If you are able to
                            get into this type of group, well I envy you, congratulations! I
                            find these are rare, probably because I sleep during the day when
                            everyone else is working, lol.</P>

            <h3>A Look at the Mastermind Structure</h3>
                <P >So now that we've covered most of what
                    a mastermind is, lets dive in a bit and look at how we can structure
                    our meetings. Generally it's good to start the group off on a
                    positive note. So it's a good idea for everyone to share something
                    that they are winning at, something that can get everyone pumped to
                    see you doing well. Then transition into a hot seat, and end with
                    accountability, some action plans for the next month or next
                    week.<BR><BR>What is a hot seat? Seriously, sounds dangerous. A hot
                    seat is not always easy to be in, that's why it's called the hot
                    seat. It's a time for others to drill into your business and
                    discover how you can make it better. If you're in the hot seat, it's
                    good to come prepared with a specific problem you would like the
                    group to work on (and by 'work on' I mean contribute ideas...not do
                    your work for you :P)</P>

                    <h4>One Hot Seat per Week<</h4>
                            <p>If a
                            group is small enough it can focus on just one hot seat per week,
                            and the closer you are to 4 the better. This way you have the
                            opportunity to get on the hot seat at least once a month.</P>
                    <h4>Everyone Gets a Little Time</h4>
                        <P >Another hot seat option is to have
                            everyone get 10 or 15 minutes. These are good for large groups, and
                            if you are having very few meeting per month, like one or two times
                            a month.<BR><BR>
                        </P>


        <P >As extensive as I've tried to be, I think I've barely scratched the surface.  We've seen
            the different types of masterminds, who is in a mastermind, and the
            mastermind structure. By now it should be clear that there are so
            many ways to make a mastermind work...but don't forget that the key
            is to be effective. Do what works and drop what doesn't.</p>

            <p>I would like
            to just add one bit of warning, it's important not to take anything
            personal. What we are looking for is constructive criticism and
            accountability. So please, don't be afraid to go out and give it a
            shot. Experiment, try one type of mastermind and try another. Heck,
            have more than one mastermind. If you do, you'll develop a personal
            connection with other entrepreneurs, and receive lots of good
            businesses advice.</p>

