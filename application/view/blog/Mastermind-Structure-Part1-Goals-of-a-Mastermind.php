<P>This is the first of a 3
part series that I will be publishing on how a mastermind meeting is
structured. Being in a mastermind is not rocket science, but how do
you know what to do if you've never had a clear understanding of it's
purpose? </P>
<P>Is your group setting itself up for failure because it lacks
goals? So my goal will be to help you improve your meetings, help
everyone in the group to feel comfortable hosting the meeting, and
hopefully uncover some of the key elements of having a successful
meeting.</P>
