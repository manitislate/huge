	<h2>Does Your Mastermind Have Goals?</h2>
    <p>Picture this: you just joined a mastermind and there
	is lot excitement in the group. To you, that mastermind is
	everything that you've been hoping to find. But after the fog
	clears, and the group disappoints you in little ways here and there,
	your desire to continue starts to...fall. I think that a large
	reason why so many mastermind groups tend to fail is because the
	members are not centered around a common goal. Although it may not
	be written out, every mastermind has a goal. Each member comes to
	the group with a certain level of expectation for the other members,
	and if those expectations are not met, it could mean the death of
	the group. To keep that from happening, your group should always
	focus on the same goals. That way every bodies needs are meet, and
	the group is far more productive as a whole.</P>

		<h3>Reaching Your Connection Goals</h3>
        <p>It's exciting to meet new entrepreneurs who
		pretty much share the same dreams and aspirations as you.
		Connecting with fellow entrepreneurs may be difficult at times, but
		those relationships are highly important to us. It's so encouraging
		to see our online friends succeeding in they're businesses.</P>
		<h3>Is Your Goal Idea Generation?</h3>
        <p>Another goal of masterminds is being able to pull
		the collective wisdom of each member in the group. We all have some
		information that we bring to the group that we have some sort
		advance knowledge in. The combination of sharing this knowledge is
		what creates the &ldquo;mastermind.&rdquo;</p>
        <p>Sometimes we also
		need to just come up with a bunch of ideas to consider. This is
		when the group can really get creative and brainstorm a bunch of
		ideas. Just remember that when you are brainstorming, the goal is
		to encourage creative thinking. That means that we don't criticize
		ideas, and we don't spend too much time thinking about them. Just
		gush them out on paper, and make sure that you are writing
		everything down. It's only after a brainstorming session that you
		go back and evaluate the ideas that your team has managed to come
		up with.</P>
		<h3>Masterminds Should Have an Accountability Goal</h3>
        <p>I believe that every mastermind
		session or group page needs to have some sort of accountability. Do
		the members follow through with what they say they will do? Do they
		try? We need to be concerned for our fellow entrepreneurs, be
		gentile, but don't let them slide. If they did not follow through,
		you need to do the hard job and let them know.</p>
        <p>On the flip side, if you find that you just didn't have the time, or
		motivation, or maybe you under estimated how much time you actually
		needed, what ever the reason may be...if you did not follow through
		with what you said you were going to do, then just be up front and
		honest and try not to make it a habit.</P>
		<h3>Your Mastermind Should Give Gentle Course Correction</h3>
        <p>As Entrepreneurs we're full of
		great ideas, but not all great ideas are what we should be doing.
		Also, sometimes we think we've got it all worked out. Our plan goes
		20 years into the future...but were starting off on the wrong foot
		and we don't even know it! That is why it's vital to your business
		to share your plans with the group. A good group will do the
		difficult thing and let you know that you need to change your
		plans.</p>
        <p>It's not easy to correct a fellow member of your
        mastermind team and visa versa, it's not easy to take criticism for
		our plans and ideas. The key to giving correction is to do it with
		out tearing the other person down, be careful but be honest. And
		when you are the person on the other end, remember to not take it
		as a personal criticism, but as a positive comment that will help
		you improve on what you are doing. In the end, just remember to
		keep the this type of accountability positive.  
		</P>

